        <p class="P10">
            <span id="1st" class="T5 TC">Pour les mécènes privés</span>,
            <span class="T11">
                la réduction d'impôt sur le revenu est de 66% du montant du don, dans la limite de 20% du revenu imposable.
    <br>
         Exemple : Un don de 100€ ne vous coûte en réalité que 34€.
     <br>

         En cas de dons au profit de l’Association, vous pouvez bénéficier d'une réduction d'IFI (impôt sur la fortune immobilière) égale
          à 75% des dons réalisés, dans la limite de 50 000€ (soit un versement effectif de 66 666€ = 50 000€ 100% : 75%).
     <br>

          Si votre don dépasse cette somme ou le montant de votre IFI, vous ne pouvez pas reporter l’excédent sur les années suivantes dans le cadre de l'IFI. En revanche, vous pouvez déclarer une partie de votre don (jusqu’à 66 667€)
                au titre de l’IFI et le solde au titre de l’IR (impôt sur le revenu) dans la limite de 20% de vos revenus imposables.
    <br>
                Lorsque le total des dons versés dépasse 20% de votre revenu imposable, vous pouvez reporter l'excédent sur les cinq années
                suivantes. Vous bénéficierez d'une réduction d'impôt dans les mêmes conditions.
                    <br>
         Entre le mécène et l'Association est établie une convention contenant la procédure et les conditions de la bienfaisance. A partir de
                1500€, la confirmation du don doit être fournie par écrit par le bénéficiaire.
                    <br>
                L'Association délivre un récépissé selon le formulaire type fixé par décret.
                <br>
                Comment bénéficier des réductions d'impôts?
      </span >


