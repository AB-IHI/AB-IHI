              2) Depuis janvier 2013, l'Administration générale des contributions autorise la déclaration des dons sans obligation de fournir, avec la déclaration d'impôts, par courrier ou via Internet, un reçu fiscal confirmant le montant du don. Il vous suffit d'inscrire dans la case 7VA de votre déclaration
              de revenus annuelle les sommes versées au cours de l'année d'imposition écoulée dans la limite de 20% de votre revenu imposable.
          </span>
                </p>
