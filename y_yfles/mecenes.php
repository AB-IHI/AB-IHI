        <p class="P10">
      <span class="T5 TC">Condition relative au don</span>
            <span class="T11">
        est aucune contrepartie directe ou indirecte au profit du donateur. Seuls ouvrent droit à réduction d'impôt les versements effectués dans une intention libérale, c'est-à-dire en principe, sans aucune contrepartie directe ou indirecte au profit du donateur.
            </span>
            <span class="T11">
        L'existence d'une contrepartie s'apprécie en fonction de la nature des avantages éventuellement accordés au donateur. Sur ce point, doivent être distingués :
            </span>
        </p>
        <!-- list dots-->
        <ul class="custom_ul_2">
            <li>
         <span class="T12 TC">
                les avantages au contenu purement institutionnel ou symbolique qui ne remettent
             pas en cause l'éligibilité au régime fiscal du mécénat (exemples : droit de vote aux Assemblées Générales, etc.);
         </span>

            </li>
            <li>
         <span class="T6 TC">
                les contreparties tangibles réalisées sous forme de remise de biens ou de prestation de services qui excluent, en principe toute application du régime fiscal du mécénat (exemples : remise de divers objets matériels, octroi d'avantages financiers ou commerciaux, service d'une revue, mise à disposition
             d'équipements ou installations de manière exclusive ou préférentielle, accès privilégié à des conseils, fichiers ou informations de toute nature...).
         </span>

            </li>
        </ul>
        <!--end of dotted list-->

        <p class="P10">
        <span class="T11">
            Par dérogation, il est admis que la remise de menus biens tels qu'insignes, timbres décoratifs, étiquettes personnalisées, affiches, épinglettes, cartes
            de vœux, etc. ne remet pas en cause l'éligibilité du don au bénéfice de la réduction d'impôt lorsque les biens remis par l'organisme bénéficiaire à chaque donateur
            au cours de la période de référence ont une valeur totale inférieure ou égale à 69 € TTC.
        </span><br>
            <span class="T11">
            Une telle disproportion sera caractérisée par l'existence d'un rapport de 1 à 4 entre la valeur du bien et le montant du don. La valeur des biens à
                prendre en compte est leur prix de revient TTC pour l'Association.
            </span><br>
            <span class="T11">
                Par exemple:
            </span>
        </p>
        <!-- list dots-->
        <ul class="custom_ul_2">
            <li>
         <span class="T12 TC">
             un don de 120€ est éligible au bénéfice de la réduction d'impôt s'il est accompagné de la livraison d'un bien d'une valeur n'excédant pas 30€
             (dans un rapport de 1 à 4 au maximum);
         </span>

            </li>
            <li>
         <span class="T6 TC">
             en revanche, un don de 460€ est éligible au bénéfice de la réduction d'impôt s'il est accompagné de la livraison d'un bien d'une valeur n'excédant pas 69€ (malgré le fait que le rapport de 1 à 4 soit de 115€).
         </span>

            </li>
        </ul>
        <!--end of dotted list-->

        <p class="P10">
        <span class="T11">
            Les versements éligibles au mécénat peuvent prendre différentes formes :
        </span>
        </p>
        <!-- list dots-->
        <ul class="custom_ul_2">
            <li>
         <span class="T12 TC">
            des dons manuels en espèces (billets, pièces, chèques) ou en nature (œuvres d'art, meubles, matériels informatiques, parts sociales, etc.);
         </span>
            </li>
            <li>
         <span class="T6 TC">

l'abandon exprès de sommes ou produits, par exemple, la non-perception de loyers (mise à disposition gratuite d'un local) ou l'abandon de fonds d'épargne solidaire ou de fonds de partage;
</span>
            </li><br>
            <li>
         <span class="T6 TC">

les frais engagés par des bénévoles pour la réalisation des missions de l'Association (et non remboursés par l'Association), frais dûment justifiés et enregistrés dans la comptabilité de l'Association et expressément abandonnés par les bénévoles; l’Association devra établir une attestation d'abandon du remboursement des frais et de joindre une déclaration écrite appropriée du bénévole comme justificatif fiscal.
</span>
            </li><br>
        </ul>
        <!--end of dotted list-->

