        à l'Association constitue un don sous un régime du mécénat lorsque le versement est effectué sans contrepartie directe ou indirecte de l'Association.</span
            >
        </p>
        <p class="P10">
        <span class="T11">
            Du côté du donateur, le régime du mécénat peut être utilisé par toutes les entreprises soumises à:
        </span>
        </p>
        <!--list-->
        <ul class="hide_list">
            <li class="hide_list">
                <p class="P11" >
          <span class="T11">
              1) l'impôt sur les sociétés (IS) conformément au régime réel,
          </span>
                </p>
            </li>
            <li class="hide_list">
                <p class="P11" >
          <span class="T6 TC">
              2) l'impôt sur le revenu (IR).
          </span>
                </p>
            </li>
        </ul>
        <!--end of list-->
        <p class="P10">
        <span class="T11">
             Conformément à la législation sur le mécénat et à l'article
            <a href="https://www.legifrance.gouv.fr/codes/article_lc/LEGIARTI000038032278/2019-12-31">238bis CGI</a>, l’entreprise
            peut déduire de ces impôts 60% de votre don, dans la limite de 0,5%
            du chiffre d'affaires fiscal contrôlé. De plus, le trop-perçu peut être reporté sur les cinq années suivantes.
        </span>
        </p>
        <p class="P10">
        <span class="T11">
            Exemple : hypothèse de chiffre d'affaires de l'entreprise: 600000€.
        </span><br>
            <span class="T11">
            Calcul de la limite: 0,5% · 600000€ = 3000€.
            </span><br>
            <span class="T11">
            La réduction d'impôt de 60% en année N est donc limitée à un don de 3000 €.
            </span>
        </p>
        <p class="P10">
        <span class="T11">
                Cas 1: L'entreprise fait un don de 3000€.
        </span><br>
            <span class="T11">
                Ainsi, il peut déclarer la totalité de son don pour l'année N et bénéficier d'une réduction d'impôt de 60%.
            </span><br>
            <span class="T11">
                60% x 3000€= 1800€
            </span><br>
            <span class="T11">
                Elle obtient donc 1800€ de détaxe.
            </span>
        </p>
        <p class="P10">
        <span class="T11">
                Cas2: Une entreprise fait un don supérieur à 3000€ (soit plus de 0,5% de son chiffre d'affaires) en année N.
        </span><br>
        </p>

        <p class="P10">
        <span class="T11">
                Si une entreprise fait un don, par exemple, de 4500€, elle déclare 3000€ l'année N, soit le plafond autorisé par la loi, et bénéficie d'une remise de 1800€ (60% · 3000€) l'année N.
        </span><br>
        </p>

        <p class="P10">
        <span class="T11">
                Elle déclare alors les 1500€ de don restant pour l'exercice suivant et bénéficiera donc d'un crédit d'impôt de 900€ (60% · 1500€) en année N+1.
        </span><br>
        </p>

        <p class="P10">
        <span class="T11">
                A compter du 1er janvier 2019, les entreprises ayant effectué, au cours de la période de déclaration, des dons, en espèces ou en nature, supérieurs à 10000 € ouvrant droit à réduction d'impôt doivent
            déclarer ce don à l'administration fiscale (<a href="https://www.impots.gouv.fr/formulaire/2069-m-fc-sd/fiche-daide-au-calcul-reduction-dimpot-mecenat">
                Déclaration des réductions et crédits d'impôt n°2069-RCS-SD, certificat n°5252</a>).        </span><br>
            <span class="T11">
La non-déclaration est passible d'une amende de 1500€.
