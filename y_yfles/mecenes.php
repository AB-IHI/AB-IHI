        <h1 id="2nd" class="T4U Tcustom pb-2 border-bottom">
            Le régime fiscal de mécénats
        </h1>

        <!-- 1st paragraph-->
        <p class="P5 T6 TC">
      <span >
          Les dons à l'Association donnent droit à une réduction d'impôt sur le revenu.
      </span>
        </p>
        <!-- end of 1st paragraph -->

        <!-- 1st paragraph-->
        <p class="P5 T6 TC">
      <span >
           <a href="https://www.insee.fr/fr/metadonnees/definition/c2088">Les impôts sur le revenu</a> sont des impôts qui
          frappent les revenus, les bénéfices et les gains en capital. Ils sont établis sur les revenus
          effectifs ou présumés des personnes physiques, ménages, sociétés et institutions sans but lucratif
          au service des ménages.
      </span>
        </p>
        <!-- end of 1st paragraph -->

        <!-- 1st paragraph-->
        <p class="P5 T6 TC">
      <span >
           Ils comprennent les impôts sur le patrimoine (terrains, immeubles, etc.) lorsque ceux-ci servent de base à l'estimation
          du revenu de leurs propriétaires. </br> Pour les ménages, cet impôt comprend essentiellement l'impôt sur le revenu des
          personnes physiques (IRPP), la contribution sociale généralisée (CSG) et la contribution pour le remboursement de la
          dette sociale (CRDS). Pour le domaine des entreprises, il s'agit les impôt: </span>
        </p>
        <!-- end of 1st paragraph -->
        <!--list-->
        <ul class="hide_list">
            <li class="hide_list">
                <p class="P11" >
          <span class="T11"
          >1) sur les sociétés (IS) conformément au régime réel,  </span
          >
                </p>
            </li>
            <li class="hide_list">
                <p class="P11" >
          <span class="T6 TC"
          >2) ou sur le revenu (IR).  </span
          >
                </p>
            </li>
        </ul>
        <!--end of list-->
                <p class="P6_borderStart" />        <p class="P8" /><!--probably smaller spacer-->
        <!--links-->
        <p class="P14">
            <span class="T12U"> En savoir + :</span><span class="T14"><br /></span>
            <a href="https://www.insee.fr/fr/metadonnees/definition/c2088"
